public class JavaDersi13 {
    public static void main(String[] args) {

        // Do-While
        int i = 2;
        do {
            
            System.out.println("Loglandı");
            
            System.out.println(i);
            i += 2;

        } while (i < 10);
        System.out.println("Do-While Döngüsü Bitti");
    }
}
