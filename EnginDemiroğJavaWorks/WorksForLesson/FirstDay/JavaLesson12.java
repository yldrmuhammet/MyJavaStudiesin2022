package FirstDay;

public class JavaLesson12 {
    public static void main(String[] args) {

        // For Döngüsü
        for (int i = 2; i < 10; i++) {
            System.out.println(i);
        }
        System.out.println("For Döngüsü Bitti");

        int i = 1;
        // While Döngüsü
        while (i < 10) {
            System.out.println(i);
            i++;
        }

        // İnfinity Loop
        System.out.println("While Döngüsü Bitti");
    }
}
